
public class UltimaLetra {

	
private static void UltimaLetra(String frase) {
		
		System.out.println("La ultima letra de la frase es: " +frase.charAt(frase.length()-1));
	}
	
	
	
	
	public static void main(String[] args) {
		// Crea una función que devuelva la última letra de una cadena de texto. Probar esta función para calcular la última letra de la frase "Hola".

		UltimaLetra("Hola");
		
	}

}


